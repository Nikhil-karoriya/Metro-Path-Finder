function show() {
    const s = document.querySelector("#Source").value;
    const d = document.querySelector("#Dest").value;
    let path = main(s, d);

    let ele = document.getElementById("paths");
    let dist = document.getElementById("dist");

    ele.innerHTML = "";
    if (!path || path.length === 0) {
        ele.innerHTML = "No Path Obtained";
        return;
    }

    // Add the starting station at the end of the path (since main() returns path in reverse order)
    path = path.reverse();
    path.push(d);

    for (let i = 0; i < path.length; i++) {
        const card = document.createElement('div');
        card.className = 'station-card';

        // Station name
        const name = document.createElement('div');
        name.textContent = path[i];
        name.style.fontWeight = "bold";
        name.style.fontSize = "1.2em";
        card.appendChild(name);

        // Action text
        const action = document.createElement('div');
        action.className = 'station-action';
        if (i === path.length - 1) {
            action.textContent = "you have reached";
        } else {
            action.textContent = `go to ${path[i + 1]}`;
        }
        card.appendChild(action);

        ele.appendChild(card);
    }

    dist.innerHTML = "";
    const dele = document.createElement("div");
    dele.innerHTML = `&rarr; Shortest Distance from <span style="color: #c0392b; font-weight: bold;">${s}</span> to <span style="color: #c0392b; font-weight: bold;">${d}</span> is <span style="color: #c0392b; font-weight: bold;">${Totaldis} units</span>.`;
    dist.append(dele);
}

