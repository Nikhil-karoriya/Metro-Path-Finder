class PriorityQueue {
    constructor() {
        this.items = [];
    }

    enqueue(element) {
        this.items.push(element);
        this.items.sort((a, b) => a.distance - b.distance); // Sort based on distance
    }

    dequeue() {
        return this.items.shift();
    }

    isEmpty() {
        return this.items.length === 0;
    }
}

